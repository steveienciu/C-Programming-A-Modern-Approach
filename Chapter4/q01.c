/* Program that takes 2 digit number and prints the reverse */

#include <stdio.h>

int main(void)
{
    int num, d1, d2;

    printf("Enter a two-digit number: ");
    scanf("%2d", &num);

    d1 = num % 10;
    d2 = (num - d1) / 10;

    printf("The reversal is: %1d%1d", d1, d2);

    return 0;
}
